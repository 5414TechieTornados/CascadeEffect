void handleCoor(float newCoor, float speed, bool axis){
	float difference;

	if(axis){
		difference = newCoor - xCoor;
  }
	else{
 		difference = newCoor - yCoor;
	}
	if(difference != 0){
		//North Direction
		if(direction == north){
			//Positive
			if((difference/abs(difference)) == 1){
				//x axis
				if(axis){
					turnRight();
				}
				//y axis
				else{
				}
			}
			//Negative
			else{
				//x axis
				if(axis){
					turnLeft();
				}
				//y axis
				else{
					turnLeft();
					turnLeft();
				}
			}
		}
		//South Direction
		else if(direction == south){
			//Positive
			if((difference/abs(difference)) == 1){
				//x axis
				if(axis){
					turnLeft();
				}
				//y axis
				else{
					turnLeft();
					turnLeft();
				}
			}
			//Negative
			else{
				//x axis
				if(axis){
					turnRight();
				}
				//y axis
				else{
				}
			}
		}
		//East Direction
		else if(direction == east){
			//Positive
			if((difference/abs(difference)) == 1){
				//x axis
				if(axis){
			}
			//y axis
				else{
					turnLeft();
				}
			}
			//Negative
			else{
				//x axis
				if(axis){
					turnLeft();
					turnLeft();
				}
				//y axis
				else{
					turnRight();
				}
			}
		}
		//West Direction
		else if(direction == west){
			//Positive
			if((difference/abs(difference)) == 1){
				//x axis
				if(axis){
					turnLeft();
					turnLeft();
				}
				//y axis
				else{
					turnRight();
				}
			}
			//Negative
			else{
				//x axis
				if(axis){
				}
				//y axis
				else{
					turnLeft();
				}
			}
		}
		moveForward(difference, axis, speed);
	}
}

