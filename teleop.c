void initializeRobot()
{

  // Place code here to initialize servos to starting positions.
  // Sensors are automatically configured and setup by ROBOTC. They may need a brief time to stabilize.
  	servoTarget[bottomGate] = BOTTOM_GATE_DOWN;
	servoTarget[topGate] = TOP_GATE_DOWN;
	servoTarget[tube] = 150;

  	return;
}

/*
Does the calculation to turn the joystick values into motor values
*/
int getMotorOutput(int joystickValue){

	int directionValue = joystickValue / abs(joystickValue);
 	float ratio = (joystickValue * joystickValue) / (MAX_JOYSTICK * MAX_JOYSTICK);
 	if(fasterSpeed){
		return (ratio * (MOTOR_POWER)) * directionValue;
	}
	else{
		return (ratio * (SLOW_MOTOR_POWER)) * directionValue;
	}
}

/*Sets the speed between fast and slow
*/
void setSpeed(){
	if(joy1Btn(1)){
		fasterSpeed = false;
	}
	else if(joy1Btn(3)){
		fasterSpeed = true;
	}
}

/*
Method that assigns the correct values to drive the motors
*/
void drive()  {
	setSpeed();
	motor[right] = getMotorOutput(joystick.joy1_y2);
	motor[left] = getMotorOutput(joystick.joy1_y1);
}

void moveBallLift()  {
	if(joy2Btn(8)){
		motor[collector] = BALL_COLLECTOR_UP;
	}
	else if(joy2Btn(7)){
		motor[collector] = BALL_COLLECTOR_DOWN;
	}
	else{
		motor[collector] = 0;
	}
}

void ballCollection(){
	if(joystick.joy2_TopHat == 0){
		motor[belts] = 0;
	}
	else if(joystick.joy2_TopHat == 4){
		motor[belts] = -100;
	}
	else if(joystick.joy2_TopHat == 2){
		motor[belts] = -50;
	}
	else if(joystick.joy2_TopHat == 6){
		motor[belts] = 100;
	}

	if(joy2Btn(2)){
		motor[lift] = BALL_LIFT_UP;
	}
	else if(joy2Btn(4)){
		motor[lift] = BALL_LIFT_DOWN;
	}
	else{
		motor[lift] = 0;
	}
}

void launchBall(){
	if(joy2Btn(3)){
		motor[launcher] = BALL_LAUNCHER_UP;
	}
	else if(joy2Btn(1)){
		motor[launcher] = BALL_LAUNCHER_DOWN;
	}
	else{
		motor[launcher] = 0;
	}
}

void moveGate(){
	if(joy2Btn(6)){
		servoTarget[topGate] = TOP_GATE_UP;
	}
	else if(ServoValue(topGate) > 165){
		servoTarget[topGate] = TOP_GATE_DOWN;
	}

	if(joy2Btn(10)){
		servoTarget[bottomGate] = BOTTOM_GATE_UP;
	}
	else if(ServoValue(bottomGate) > 165){
		servoTarget[bottomGate] = BOTTOM_GATE_DOWN;
	}

}

void moveTubeServo(){
	if(joy1Btn(4)){
		servoTarget[tube] = 150;
	}
	else if(joy1Btn(2)){
		servoTarget[tube] = 230;
	}
}


task main()
{
	// wait for start of tele-op phase
	waitForStart();
	initializeRobot();

	bFloatDuringInactiveMotorPWM = false;

  //Game loop for robot
  while (true){
  	getJoystickSettings(joystick);

		drive();
		moveBallLift();
		launchBall();
		ballCollection();
		moveGate();
		moveTubeServo();
	}
}
